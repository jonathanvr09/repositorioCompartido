// OPERADORES DE COMPARACION

// Comparación simple

console.log( 5 == '5');
 
// Comparación estricta

console.log( 5 === '5');

// Desigualdad simple

console.log( 5 != '5');

// Desigualdad estricta

console.log( 5 !==  '5');

// Mayor que..

console.log( 6 > 6 );

// Mayor o igual que..

console.log( 6 >= 6 );

// Menor que..

console.log( 7 <7 );

// Menor o igual que..

console.log( 7 <= 7 );

// AND  &&

console.log( "===========" );
console.log( false && true );
console.log( false && false );
console.log( true && true );
console.log( false && (3 == 4) );
console.log( "===========" );

// OR  || 

console.log( "===========" );
console.log( true || true );
console.log( false || true );
console.log( false || false );
console.log( "===========" );


// Otros ejemplos &&,  || 

console.log( false && (3 == 4) );
console.log("Gato" && "Perro");
console.log("Gato" || "Perro");
console.log( false | 3 == 4 );
console.log('Zapato' == 'trampa' || "hola" <= "chau");
console.log( "===========" );


