// OPERADORES MATEMATICOS

// Suma

console.log( 5 + 4 );

// Resta

console.log( 5 - 4 );

// Multiplicación

console.log( 5 * 4 );

// División

console.log( 5 / 4 );

// Módulo

console.log( 5 % 4 );

// Incremento

let num1 = 5;
console.log( ++num1 );

// Decremento

let num2 = 4;
console.log( --num2 );

